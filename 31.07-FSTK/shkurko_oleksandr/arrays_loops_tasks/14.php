<p>Дан массив с элементами 4, 2, 5, 19, 13, 0, 10. С помощью цикла foreach и оператора if
    проверьте есть ли в массиве элемент со значением $e, равном 2, 3 или 4. Если есть —
    выведите на экран 'Есть!', иначе выведите 'Нет!'.</p>
<?php
$arr = [4, 2, 5, 19, 13, 0, 10];
$b = [2,3,4];
foreach ($arr as $value) {
    foreach ($b as $num) {
        if ($value == $num) {
            $res = "Есть!";
        }
    }
}
echo $res